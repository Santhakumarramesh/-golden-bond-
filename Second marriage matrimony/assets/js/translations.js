// Current language
let currentLanguage = 'en';

/**
 * Set the current language and update all translations
 * @param {string} lang - Language code (en, hi, ta, ar, es, fr)
 */
function setLanguage(lang) {
  if (!translations[lang]) {
    console.warn(`Language '${lang}' not found, falling back to English`);
    lang = 'en';
  }
  
  currentLanguage = lang;
  localStorage.setItem('soulmatch_language', lang);
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = translations[lang][key];
    
    if (translation) {
      // Check if translation contains HTML
      if (translation.includes('<')) {
        element.innerHTML = translation;
      } else {
        element.textContent = translation;
      }
    }
  });
  
  // Update language selector if exists
  const langSelect = document.getElementById('languageSelect');
  if (langSelect) {
    langSelect.value = lang;
  }
  
  // Update document direction for RTL languages
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.body.classList.add('rtl');
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.body.classList.remove('rtl');
  }
  
  // Dispatch event for other scripts to listen
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

/**
 * Get translation for a specific key
 * @param {string} key - Translation key
 * @param {string} [lang] - Language code (optional, uses current language if not provided)
 * @returns {string} - Translated text or key if not found
 */
function t(key, lang = currentLanguage) {
  return translations[lang]?.[key] || translations['en']?.[key] || key;
}

/**
 * Get current language
 * @returns {string} - Current language code
 */
function getCurrentLanguage() {
  return currentLanguage;
}

/**
 * Detect browser language and set appropriate language
 */
function detectLanguage() {
  const browserLang = navigator.language.split('-')[0];
  const supportedLangs = Object.keys(translations);
  
  if (supportedLangs.includes(browserLang)) {
    return browserLang;
  }
  
  return 'en';
}

/**
 * Initialize language system
 */
function initLanguage() {
  // Check for saved language preference
  const savedLang = localStorage.getItem('soulmatch_language');
  
  if (savedLang && translations[savedLang]) {
    setLanguage(savedLang);
  } else {
    // Detect and set language
    const detectedLang = detectLanguage();
    setLanguage(detectedLang);
  }
  
  // Set up language selector event listener
  const langSelect = document.getElementById('languageSelect');
  if (langSelect) {
    langSelect.addEventListener('change', (e) => {
      setLanguage(e.target.value);
    });
  }
}

// Export for use in other modules
window.SoulMatchTranslations = {
  setLanguage,
  t,
  getCurrentLanguage,
  initLanguage,
  translations
};

